package com.alphonso.thingword.zhibao.util;

public class PlantType {
    private String type;
    private String detail;
    private int type_index;
    private int detail_index;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public int getType_index() {
        return type_index;
    }

    public void setType_index(int type_index) {
        this.type_index = type_index;
    }

    public int getDetail_index() {
        return detail_index;
    }

    public void setDetail_index(int detail_index) {
        this.detail_index = detail_index;
    }
}
